function openAbout() {
  var ab = document.getElementById("pageAbout");
  if (ab.style.visibility === "hidden") {
    ab.style.visibility = "unset";
  } else {
    ab.style.visibility = "hidden";
  }
}

function openGe() {
  var a = document.getElementById("page1");
  if (a.style.transform === "translate(100vh, 100vw)") {
    a.style.transform = "translate(0vh, 0vw)";
  } else {
    a.style.transform = "translate(100vh, 100vw)";
  }
}

